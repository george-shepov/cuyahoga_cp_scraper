#!/bin/bash
# Download CR indictment PDFs for all September 2023 cases missing them
# Generated automatically - 121 cases
set -euo pipefail
SCRAPER_DIR="$(cd "$(dirname "$0")" && pwd)"
cd "$SCRAPER_DIR"

LOG="batch_sep_download.log"
echo "=== Sep 2023 CR PDF batch download started $(date) ===" | tee -a "$LOG"

DONE=0
FAIL=0
SKIP=0

download_case() {
    local case_id="$1"
    local num="${case_id#CR-23-}"
    num="${num%-A}"
    
    # Check if already done since script started
    local pdf_dir="out/2023/pdfs/${case_id}"
    if ls "${pdf_dir}"/*_CR_*.pdf 2>/dev/null | head -1 | grep -q .; then
        echo "SKIP $case_id (already has CR PDF)" | tee -a "$LOG"
        ((SKIP++)) || true
        return 0
    fi
    
    echo "--- Downloading $case_id ---" | tee -a "$LOG"
    if python3 main.py scrape --year 2023 --start "$num" --limit 1 \
        --direction up --headless --delay-ms 800 \
        --download-pdfs --pdf-types CR 2>> "$LOG"; then
        ((DONE++)) || true
        echo "OK $case_id" | tee -a "$LOG"
    else
        ((FAIL++)) || true
        echo "FAIL $case_id" | tee -a "$LOG"
    fi
    sleep 1
}

